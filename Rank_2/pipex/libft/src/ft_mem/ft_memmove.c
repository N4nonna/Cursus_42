#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	size_t	i;
	int		len;

	if (dest == src)
		return (dest);
	i = 0;
	if (dest < src)
	{
		while (i < n)
		{
			((unsigned char *)dest)[i] = ((unsigned char *)src)[i];
			i++;
		}
	}
	else
	{
		while (i < n)
		{
			len = n - 1 - i;
			((unsigned char *)dest)[len] = ((unsigned char *)src)[len];
			i++;
		}
	}
	return (dest);
}
